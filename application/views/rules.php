<h2>Rules &amp; Regulations</h2>
<h3>WHO IS ELIGIBLE?</h3>
<p>
Students who are currently enrolled at the following schools:<br />
ECUAD - Emily Carr University of Art &amp; Design<br />
NSCAD - Nova Scotia College of Art &amp; Design<br />
VCAD - Vancouver College of Art &amp; Design<br />
</p>
<p>
The Competition is open to part-time as well as full-time students at the post-secondary, undergraduate level.
Employees, representatives and agents of Ryvita Canada, its advertising and promotion agencies, , and members of the immediate family (brothers, 
sisters, children, father, mother), spouses or persons living in the same household as any of the foregoing are not permitted to enter or win.
If the entrant is under the age of majority, the entrant's parent or guardian must sign and submit the Submission Form.
</p>
<h3>WHAT IS THE ENTRY PROCEDURE?</h3>
<p>
No purchase is required. To enter the Competition, students must send their completed Submission Form with the required materials to Ryvita 
Canada by February 27th, 2015 (see address below, question # 6).
</p>
<h3>THE SUBMISSION MATERIALS ARE:</h3>
<p>
<ol type="i">
<li>Digital images of the work of art being submitted in a PC/MAC compatible jpg or pdf file, with a resolution of 300 DPI or larger</li>
<li>A title of the piece being submitted</li>
<li>Artist statement of 150 - 200 words addressing your work in general and the submission in particular.</li>
<li>The artwork submitted by entrants must use the templates provided as reflect the actual scale of the Ryvita Tin.</li>
</ol>
</p>
<h3>WHAT ARE THE GUIDELINES FOR ARTWORK SUBMISSIONS?</h3>
<h3>POSITIONING / LOOK &amp; FEEL</h3>
<p>
Our core Ryvita Canadian consumer is female in the 35-54 demographic that are eager to try healthier snacks that don't sacrifice taste, 
but right choices are often hard to find. Ryvita helps make healthy eating more enjoyable with simple, easy delicious recipes that inspire 
consumers to eat healthy and be creative.
</p>
<p>
Ryvita is looking for a fashionable, trendy way to showcase our limited edition Tin across Canada that will act as a perfect accessory to keep 
your favourite Crispbreads fresh, crunchy and tasty in home.
</p>
<h3>REQUIRED DESIGN ELEMENTS &amp; CONSIDERATIONS</h3>
<p>
Ryvita logo: will be embossed and included in tin design<br />
Ryvita colours:<br />
Use of Ryvita red in the colour scheme<br />
Colour Pantone: #E0007 CMYK (0, 100%, 99%, 4%), RGB (224, 0, 7)<br />
A maximum of 4 colours can be used in the illustration<br />
</p>
<p>
Designs must represent one of the following design options:
A contemporary, yet classic and timeless design
<ul>
    <li>&quot;Why Canada loves Ryvita&quot;</li>
    <li>Taste - visually communicate how tasty Ryvita can be</li>
    <li>Versatility - showcase how Ryvita can be used & inspire people to try something different</li>
    <li>Sociability - show Ryvita Canada in a social context to help overcome social stigma</li>
</ul>
Design: As Ryvita Tin graphics will be custom-made, the preference is for one design feature that can be applied as a large panel.<br />
Designs may be one dimensional or have a base graphic with overlays.<br />
Entry submissions may be received as jpegs; final designs must be submitted as Illustrator files.<br />
</p>
<h3>HOW AND WHEN ARE THE SUCCESSFUL SUBMISSIONS FOR THE COMPETITION DETERMINED? WHAT HAPPENS AFTER THAT?</h3>
<p>
The adjudication process will take place on or before March 31, 2015.
<ul>
<li>The Selection Committee members are Ryvita Canada's Marketing team and based on consumer votes from www.ryvitatincontest.com</li>
<li>The Selection Committee will review the digital images submitted by the entrants, assesses the artwork in terms of artistic and technical 
    merit and representation of design options, and selects one submission as the winner.</li>
<li>Ryvita Canada's Marketing team will have the final decision on the winning submission</li>
</ul>
The winner will be contacted within five (5) business days after the judging takes place by email or phone. A
full-page announcement naming the winner of the Competition will be sent in a press release following the unveiling.<br />
All decisions regarding this Competition and the selection of successful submissions remain with Ryvita Canada and are final and without 
appeal. All submissions and Competition materials become property of Ryvita Canada and none will be returned.
</p>
